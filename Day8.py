cars = ['audi', 'bmw', 'toyota', 'honda', 'subaru']
cars_upper = []

print("–ê–≤—Ç–æ–º–æ–±–∏–ª–∏ –≤ —Å–∞–ª–æ–Ω–µ (—Å –∑–∞–≥–ª–∞–≤–Ω–æ–π –±—É–∫–≤—ã):")
for car in cars: 
    print(car.title())
    cars_upper.append(car.upper())

print(f"–°–ø–∏—Å–æ–∫ –º–∞—à–∏–Ω –∑–∞–≥–ª–∞–≤–Ω—ã–º–∏ –±—É–∫–≤–∞–º–∏: {cars_upper}")

available_cars = cars[:]

sold_car = available_cars.pop()

available_cars.insert(0, "lada")

print(f"–ò—Å—Ö–æ–¥–Ω—ã–π —Å–ø–∏—Å–æ–∫ –º–∞—à–∏–Ω: {cars}")
print(f"–°–ø–∏—Å–æ–∫ –¥–æ—Å—Ç—É–ø–Ω—ã—Ö –º–∞—à–∏–Ω: {available_cars}")
print(f"–ü—Ä–æ–¥–∞–Ω –∞–≤—Ç–æ–º–æ–±–∏–ª—å: {sold_car}")

car_prices = (20000, 35000, 15000)
print(f"–¶–µ–Ω—ã –Ω–∞ 3 –∞–≤—Ç–æ–º–æ–±–∏–ª—è (–∫–æ—Ä—Ç–µ–∂): {car_prices}")
print(f"–°–∞–º–∞—è –¥–æ—Ä–æ–≥–∞—è —Ü–µ–Ω–∞: {max(car_prices)}")
print(f"–°–∞–º–∞—è –¥–µ—à—ë–≤–∞—è —Ü–µ–Ω–∞: {min(car_prices)}")